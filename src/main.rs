// Функция для рисования ромба звездочками
fn draw_rhombus(size: usize) {
    // Рисуем верхнюю половину ромба (включая среднюю строку)
    for i in 0..size {
        // Количество пробелов перед звездочками
        let spaces = size - i - 1;
        // Количество звездочек в текущей строке
        let stars = 2 * i + 1;
        // Выводим строку: сначала пробелы, затем звездочки
        println!("{}{}", " ".repeat(spaces), "*".repeat(stars));
    }

    // Рисуем нижнюю половину ромба (исключая среднюю строку, т.к. она уже выведена)
    for i in (0..size-1).rev() {
        // Количество пробелов перед звездочками
        let spaces = size - i - 1;
        // Количество звездочек в текущей строке
        let stars = 2 * i + 1;
        // Выводим строку: сначала пробелы, затем звездочки
        println!("{}{}", " ".repeat(spaces), "*".repeat(stars));
    }
}

fn main() {
    // Вызов функции для рисования ромба размером 4
    draw_rhombus(4);
}

